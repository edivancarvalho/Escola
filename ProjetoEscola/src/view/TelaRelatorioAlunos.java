    private void consultarContato(List<Contato> listaC) throws Exception {
        DefaultTableModel modelo = (DefaultTableModel) tableBuscarAlunos.getModel();
        modelo.setNumRows(0);
        //aqui → chamar o metodo de ordenação.
        contatoBll.ordenaListaContato(listaC);
        // colar o metodo aqui.
        for (int pos = 0; pos < listaC.size(); pos++) {
            String[] linhaC = new String[3];
            Contato auxC = listaC.get(pos);
            linhaC[0] = auxC.getAluno().getId() + "";
            linhaC[1] = auxC.getAluno().getNome() + "";
            linhaC[2] = auxC.getTelefone() + "";
            modelo.addRow(linhaC);
        }

    }

